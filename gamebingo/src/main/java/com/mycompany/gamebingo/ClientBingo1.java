    private static void fillCard(String msgin) {
        boolean check = false;
        int x = Integer.parseInt(msgin);
        
        int[][] cn = card.getCardNumber();
        for(int i = 0; i<5; i++){
            for(int j = 0; j < 5;j++){
                if(cn[i][j]==x){
                    check = true;
                    status[i][j] = true;
                    rows[i]++;
                    cols[j]++;
                    if(i==j) mainCross++;
                    if(i+j == 4) otherCross++;
                    break;
                }
            }
            if(check) break;
        }
    }
